<article class="container no-elevate">
  <h3>Getting Started with specd</h3>
  <div class="space"></div>
  <p>
    specd is a specification-driven development tool that gives your AI coding
    agent a memory that survives the session. It stores specs, tasks, and
    reference docs in markdown files with powerful search and citations.
  </p>
  <div class="medium-space"></div>
  <p>
    Your specifications live as markdown files in the project directory. The
    database is a derived cache that gets rebuilt automatically, so the
    markdown files are always the source of truth. Edit them with any tool
    you like — specd keeps everything in sync.
  </p>
  <div class="medium-space"></div>
  <p>
    Use the navigation rail on the left to explore your project. This Web UI
    is served directly from the specd binary — no external dependencies
    required.
  </p>
</article>
